#include<iostream>
using namespace std;
int main(){
    // implicit 

    // int to float

    int a=10;
    float b=5.5;
    cout << a+b << endl;


    // char to int

    char ch='A';
    int a = ch+1;
    cout << a<< endl;

    char ch='A';
    char a = ch+1;
    cout << a<< endl;   


    // int to char

    int a=97;
    char ch=a;
    cout << ch << endl;

    // explicit

    // float to char

    float a=65.35;
    char ch=(char)a;
    cout << ch << endl;

    

}